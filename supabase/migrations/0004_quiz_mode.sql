-- ---------- 6. pure scoring (TS twin: lib/quiz-scoring.ts) ----------
-- Wrong = 0. 'flat' correct = 1000. 'speed' correct decays linearly from 1000
-- (instant) to 100 (buzzer); elapsed clamped to [0, window]; window <= 0 → 100.
create or replace function quiz.score_answer(
  p_correct boolean,
  p_points_mode text,
  p_elapsed_ms double precision,
  p_window_ms double precision
) returns int
language plpgsql
immutable
as $$
declare
  v_max   int := 1000;
  v_min   int := 100;
  v_clamp double precision;
  v_frac  double precision;
begin
  if not p_correct then return 0; end if;
  if p_points_mode = 'flat' then return v_max; end if;
  if p_window_ms <= 0 then return v_min; end if;
  v_clamp := greatest(0, least(p_elapsed_ms, p_window_ms));
  v_frac  := v_clamp / p_window_ms;
  return round(v_max - v_frac * (v_max - v_min))::int;
end;
$$;

-- ---------- 7. advance_question (host action: open next / reveal / end) ----------
-- Lifecycle of the shared stream, all server-authoritative:
--   action 'next'   : idle/reveal → open the next question ('question' phase),
--                      stamp question_started_at, advance the index. If there
--                      is no next question it ends the quiz (game → finished).
--   action 'reveal' : question → 'reveal' (close answering, show leaderboard).
--   action 'end'    : → ended + game finished (the host stops early).
-- Returns the resulting quiz_state row plus the total question count so the
-- caller knows whether the quiz finished.
create or replace function quiz.advance_question(
  p_game_id uuid,
  p_action text
) returns jsonb
language plpgsql
as $$
declare
  v_game  quiz.games%rowtype;
  v_state quiz.quiz_state%rowtype;
  v_total int;
  v_next  int;
begin
  select * into v_game from quiz.games where id = p_game_id for update;
  if not found then raise exception 'game_not_found'; end if;
  if v_game.game_type <> 'quiz' then raise exception 'not_quiz'; end if;
  if v_game.status = 'finished' then raise exception 'game_not_live'; end if;

  -- Upsert + lock the state row (lazy create on first advance).
  insert into quiz.quiz_state (game_id) values (p_game_id)
    on conflict (game_id) do nothing;
  select * into v_state from quiz.quiz_state
    where game_id = p_game_id for update;

  select count(*) into v_total from quiz.questions
    where set_id = (v_game.config ->> 'questionSetId')::uuid;

  if p_action = 'end' then
    update quiz.quiz_state set phase = 'ended' where game_id = p_game_id
      returning * into v_state;
    update quiz.games set status = 'finished', ended_at = now()
      where id = p_game_id and status <> 'finished';

  elsif p_action = 'reveal' then
    if v_state.phase <> 'question' then raise exception 'not_in_question'; end if;
    update quiz.quiz_state set phase = 'reveal' where game_id = p_game_id
      returning * into v_state;

  elsif p_action = 'next' then
    if v_state.phase = 'question' then raise exception 'question_open'; end if;
    v_next := v_state.current_index + 1;
    if v_next >= v_total then
      -- no more questions → finish the quiz
      update quiz.quiz_state set phase = 'ended' where game_id = p_game_id
        returning * into v_state;
      update quiz.games set status = 'finished', ended_at = now()
        where id = p_game_id and status <> 'finished';
    else
      -- flip the game live on the first question (lobby → live).
      update quiz.games set status = 'live',
             started_at = coalesce(started_at, now())
        where id = p_game_id and status = 'lobby';
      update quiz.quiz_state
        set current_index = v_next,
            phase = 'question',
            question_started_at = now()
        where game_id = p_game_id
        returning * into v_state;
    end if;

  else
    raise exception 'invalid_action';
  end if;

  return jsonb_build_object('state', to_jsonb(v_state), 'totalQuestions', v_total);
end;
$$;

-- ---------- 8. submit_answer (player action: tap one of 4) ----------
-- Atomic: validates the question is the one currently open, computes points
-- server-side (the elapsed time is measured against question_started_at, NEVER
-- trusted from the client), locks in exactly one answer per player+question.
-- Mirrors create_mark's "all validation inside the RPC" discipline.
create or replace function quiz.submit_answer(
  p_game_id uuid,
  p_player_id uuid,
  p_question_id uuid,
  p_choice int
) returns jsonb
language plpgsql
as $$
declare
  v_game     quiz.games%rowtype;
  v_state    quiz.quiz_state%rowtype;
  v_question quiz.questions%rowtype;
  v_set_id   uuid;
  v_correct  boolean;
  v_elapsed  double precision;
  v_window   double precision;
  v_points   int;
  v_answer   quiz.answers%rowtype;
begin
  if p_choice < 0 or p_choice > 3 then raise exception 'invalid_choice'; end if;

  select * into v_game from quiz.games where id = p_game_id;
  if not found then raise exception 'game_not_found'; end if;
  if v_game.game_type <> 'quiz' then raise exception 'not_quiz'; end if;
  if v_game.status <> 'live' then raise exception 'game_not_live'; end if;

  perform 1 from quiz.players
    where id = p_player_id and game_id = p_game_id and status = 'active';
  if not found then raise exception 'invalid_player'; end if;

  select * into v_state from quiz.quiz_state where game_id = p_game_id for update;
  if not found or v_state.phase <> 'question' then
    raise exception 'no_open_question';
  end if;

  v_set_id := (v_game.config ->> 'questionSetId')::uuid;
  -- The question being answered must be the one currently open (by index into
  -- the ordered list). Block answering a past/future question (replayed POST).
  select q.* into v_question
    from quiz.questions q
    where q.set_id = v_set_id
    order by q.sort_order, q.id
    offset v_state.current_index limit 1;
  if not found or v_question.id <> p_question_id then
    raise exception 'wrong_question';
  end if;

  v_correct := (p_choice = v_question.correct_index);
  v_window  := coalesce((v_game.config ->> 'perQuestionSeconds')::double precision, 20) * 1000;
  v_elapsed := extract(epoch from (now() - coalesce(v_state.question_started_at, now()))) * 1000;
  v_points  := quiz.score_answer(
    v_correct,
    coalesce(v_game.config ->> 'pointsMode', 'speed'),
    v_elapsed,
    v_window
  );

  begin
    insert into quiz.answers
      (game_id, question_id, player_id, choice, correct, points)
    values
      (p_game_id, p_question_id, p_player_id, p_choice, v_correct, v_points)
    returning * into v_answer;
  exception when unique_violation then
    raise exception 'already_answered';
  end;

  return to_jsonb(v_answer);
end;
$$;

