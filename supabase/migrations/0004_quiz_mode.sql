-- SundayQuiz — QUIZ MODE (the namesake): a Kahoot-style live Q&A game_type.
-- NET-NEW in-mode logic that reuses the generic shell (games/players/lifecycle,
-- PIN join, resume, presence) untouched. Shares NOTHING with bingo's marking
-- logic (spec §2). Atomic server-side scoring mirrors the mark RPC pattern from
-- 0002 (a TS twin lives in lib/quiz-scoring.ts — keep in lockstep).
--
-- Idempotent + additive: re-runnable. Drops/recreates the game_type CHECK to
-- add 'quiz', creates new tables only if absent, seeds only when empty.

-- ---------- 1. widen the game_type CHECK to admit 'quiz' ----------
-- Constraint name is generated by Postgres for an inline `check (...)`; find
-- and drop whichever CHECK governs game_type, then re-add the widened one. This
-- is safe to re-run (the new constraint is dropped first if present).
do $$
declare
  c record;
begin
  for c in
    select con.conname
    from pg_constraint con
    join pg_class rel on rel.oid = con.conrelid
    join pg_namespace ns on ns.oid = rel.relnamespace
    where ns.nspname = 'quiz'
      and rel.relname = 'games'
      and con.contype = 'c'
      and pg_get_constraintdef(con.oid) ilike '%game_type%'
  loop
    execute format('alter table quiz.games drop constraint %I', c.conname);
  end loop;
  alter table quiz.games
    add constraint games_game_type_check
    check (game_type in ('bingo', 'quiz'));
end $$;

-- ---------- 2. question sets (parallel to statement_sets) ----------
create table if not exists quiz.question_sets (
  id            uuid primary key default gen_random_uuid(),
  owner_user_id uuid,                                              -- reserved for suite auth (v2)
  game_id       uuid references quiz.games (id) on delete cascade, -- custom set: lives with its game
  title         text not null check (length(title) between 1 and 80),
  audience      text not null default 'generell' check (audience in ('kirke','skole','generell')),
  is_builtin    boolean not null default false,
  language      text not null default 'nb',
  created_at    timestamptz not null default now()
);
create index if not exists question_sets_game_idx
  on quiz.question_sets (game_id) where game_id is not null;

create table if not exists quiz.questions (
  id            uuid primary key default gen_random_uuid(),
  set_id        uuid not null references quiz.question_sets (id) on delete cascade,
  prompt        text not null check (length(prompt) between 1 and 300),
  -- exactly 4 answer options, index 0..3 → coloured tile on the board.
  options       jsonb not null,
  correct_index int not null check (correct_index between 0 and 3),
  sort_order    int not null default 0,
  -- 4 options exactly; jsonb_array_length guards a malformed seed/custom set.
  constraint questions_four_options check (jsonb_array_length(options) = 4)
);
create index if not exists questions_set_idx on quiz.questions (set_id);

-- ---------- 3. live quiz state (one row per quiz game) ----------
-- The host advances a single shared question stream; this row is the
-- authoritative cursor + phase + question-open timestamp (drives speed scoring
-- and the board countdown). Created lazily on first advance.
create table if not exists quiz.quiz_state (
  id                  uuid primary key default gen_random_uuid(),
  game_id             uuid not null unique references quiz.games (id) on delete cascade,
  current_index       int not null default -1,                       -- -1 before the first question
  phase               text not null default 'idle'
                        check (phase in ('idle','question','reveal','ended')),
  question_started_at timestamptz
);

-- ---------- 4. answers (one per player per question; audit + score source) ----------
create table if not exists quiz.answers (
  id          uuid primary key default gen_random_uuid(),
  game_id     uuid not null references quiz.games (id) on delete cascade,
  question_id uuid not null references quiz.questions (id) on delete cascade,
  player_id   uuid not null references quiz.players (id) on delete cascade,
  choice      int not null check (choice between 0 and 3),
  correct     boolean not null,
  points      int not null default 0,
  answered_at timestamptz not null default now(),
  -- one answer per player per question (lock-in; no changing your mind).
  unique (game_id, question_id, player_id)
);
create index if not exists answers_game_idx on quiz.answers (game_id);
create index if not exists answers_question_idx on quiz.answers (game_id, question_id);

